    public String gs() {
        String b="";
        ArrayList<String> n = new ArrayList<String>();
        String s="";
        if (Haw.isSelected()) {
            s="Hawaina";
            n.add(s);
        }
        if (Pep.isSelected()) {
            s=" Peperoni";
            n.add(s);
        }
        if (Mex.isSelected()) {
            s=" Mexicana";
            n.add(s);
        }

        if (Ranch.isSelected()) {
            s=" Ranchera";
            n.add(s);
        }
        if (Pas.isSelected()) {
            s=" Pastor";
            n.add(s);
        }
        if (Champ.isSelected()) {
            s=" Champi√±on";
            n.add(s);
        }
        for (int i = 0; i < (n.size()-1); i++) {
            b=n.get(i);
            n.remove(i);
            n.add(i, b);
        }
        b=n.toString();
        return b;
    }
